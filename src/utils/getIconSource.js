export const getIconSource = (weather) => {
  if (weather === 'clear-day') {
    return '/images/clear-day.png';
  }
  if (weather === 'clear-night') {
    return '/images/clear-night.png';
  }
  if (weather === 'cloudy') {
    return '/images/cloudy.png';
  }
  if (weather === 'fog') {
    return '/images/fog.png';
  }
  if (weather === 'hail') {
    return '/images/hail.png';
  }
  if (weather === 'partly-cloudy-day') {
    return '/images/partly-cloudy-day.png';
  }
  if (weather === 'partly-cloudy-night') {
    return '/images/partly-cloudy-night.png';
  }
  if (weather === 'rain') {
    return '/images/rain.png';
  }
  if (weather === 'rain-snow') {
    return '/images/rain-snow.png';
  }
  if (weather === 'rain-snow-showers-day') {
    return '/images/rain-snow-showers-day.png';
  }
  if (weather === 'rain-snow-showers-night') {
    return '/images/rain-snow-showers-night.png';
  }
  if (weather === 'showers-day') {
    return '/images/showers-day.png';
  }
  if (weather === 'showers-night') {
    return '/images/showers-night.png';
  }
  if (weather === 'sleet') {
    return '/images/sleet.png';
  }
  if (weather === 'snow') {
    return '/images/snow.png';
  }
  if (weather === 'snow-showers-day') {
    return '/images/snow-showers-day.png';
  }
  if (weather === 'snow-showers-night') {
    return '/images/snow-showers-night.png';
  }
  if (weather === 'thunder') {
    return '/images/thunder.png';
  }
  if (weather === 'thunder-rain') {
    return '/images/thunder-rain.png';
  }
  if (weather === 'thunder-showers-day') {
    return '/images/thunder-showers-day.png';
  }
  if (weather === 'thunder-showers-night') {
    return '/images/thunder-showers-night.png';
  }
  if (weather === 'wind') {
    return '/images/wind.png';
  }
};
